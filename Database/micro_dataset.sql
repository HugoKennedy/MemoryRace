/* Ajout des 30 photos de test  séparés entre 3 courses imaginaires */

INSERT INTO Photos (date,id_course,latitude,longitude,file_path)
WITH RECURSIVE numbers AS (
    SELECT 0 AS num
    UNION ALL
    SELECT num + 1 FROM numbers WHERE num < 29
)
SELECT  CASE 
            WHEN num < 10 THEN '2025-04-10'
            WHEN num < 20 THEN '2025-03-09'
            ELSE '2025-03-23'
        END AS date,
        CASE 
            WHEN num < 10 THEN 1
            WHEN num < 20 THEN 2
            ELSE 3
        END AS id_course,
        CASE 
            WHEN num < 10 THEN 48.266667
            WHEN num < 20 THEN 48.866669
            ELSE 49.01667
        END AS latitude,
        CASE 
            WHEN num < 10 THEN 3.25
            WHEN num < 20 THEN 2.33333
            ELSE 2.25
        END AS longitude,
        CONCAT('Database/Micro_dataset/images/', num, '.png')
FROM numbers;

